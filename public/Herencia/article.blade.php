          <h3 class="titulos" align="center" style="border-bottom: solid 1px;">Modos de Juego</h3>
            <p>El juego cuenta con tres gamemodes principales - <strong>situaciones, multijugador, y la caza del terrorista.</strong><br>El gamemode Situations cuenta con diez misiones tutoriales que permiten a los jugadores practicar conceptos de juego fuera de línea contra bots y obtener una mejor familiaridad con el juego.</p>

            <p>Multijugador cuenta con tres sub gamemodes - <strong>Bomba, Hostage, y Secure Area.</strong> Todos los gamemodes juegan muy similar, pero tienen objetos radicalmente diferentes, siendo relativamente auto-explicativo. Con cada partido siendo 5v5, los jugadores tienen menos de cuatro minutos por ronda para asegurar su objetivo, con cada equipo que necesita ganar al menos tres rondas para ganar el partido.</p>

            <img src="imagenes/ImagenModoJuego.jpg" style="width:30%; height: auto;float: right; margin-left: 1%;">
              <h4 class="titulos" align="center" ><strong>Clasificado</strong></h4>
            
            <p>Clasificado es la lista multijugador competitiva que se ofrece en Rainbow Six Siege, que se pone a disposición de los jugadores una vez que alcanzan el nivel 20. Aunque esencialmente lo mismo que casual multijugador, clasificado naturalmente características más competitivo juego y ofrece algunos pequeños cambios. Por ejemplo, los jugadores tienen la opción de elegir su spawn en lugar de ser al azar y ser clasificado en función de su rendimiento contra el equipo enemigo. Los jugadores deben ganar por lo menos dos rondas más que el equipo contrario para asegurar una victoria, lo que significa que un equipo necesita ganar al menos cuatro rondas, lo que permite un total máximo de nueve rondas que se pueden jugar.</p>

              <p>El PVE gamemode <strong>Terrorist Hunt</strong> vuelve de las versiones pasadas de la serie, compartiendo muchos gamemodes con Multiplayer. También cuenta con el "clásico" gamemode que los jugadores de las tareas con la eliminación de un número determinado de terroristas basados ​​en la dificultad que se han fortificado en un mapa.</p>
          
